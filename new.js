// Create a sample calculator

function add(a, b) {
    return a + b;
}

function subtract(a, b) {
    return a - b;
}



// Example usage
console.log("Addition: ", add(5, 3)); // 8
console.log("Subtraction: ", subtract(5, 3)); // 2
